package model;

public class Traveler {

	private String travelerName;
	private String travelerDocumentType;
	private String travelerDocumentId;
	private String travelerPhone;
	public String getTravelerName() {
		return travelerName;
	}
	public void setTravelerName(String travelerName) {
		this.travelerName = travelerName;
	}
	public String getTravelerDocumentType() {
		return travelerDocumentType;
	}
	public void setTravelerDocumentType(String travelerDocumentType) {
		this.travelerDocumentType = travelerDocumentType;
	}
	public String getTravelerDocumentId() {
		return travelerDocumentId;
	}
	public void setTravelerDocumentId(String travelerDocumentId) {
		this.travelerDocumentId = travelerDocumentId;
	}
	public String getTravelerPhone() {
		return travelerPhone;
	}
	public void setTravelerPhone(String travelerPhone) {
		this.travelerPhone = travelerPhone;
	}
}
